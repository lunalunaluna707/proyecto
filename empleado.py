class Empleado:
    def __init__(self, nombre, numero, telefono, domicilio, curp, nss, rfc, correo):
        self.nombre = nombre
        self.numero = numero
        self.telefono = telefono
        self.domicilio = domicilio
        self.curp = curp
        self.nss = nss
        self.rfc = rfc
        self.correo = correo
